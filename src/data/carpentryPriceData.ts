export const getExtraFaqs = (slug: string, area: string, locale: 'sv' | 'en'): ExtraFaq[] => {
  const data = CARPENTRY_EXTRA_FAQS[slug];
  if (!data) return [];
  const faqs = locale === 'sv' ? data.sv : data.en;
  return faqs.map(faq => ({
    q: faq.q.replace(/\{area\}/g, area),
    a: faq.a.replace(/\{area\}/g, area),
  }));
};
